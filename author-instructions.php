                        <h4>AIM & SCOPE</h4>
                        <div class="spacer-15"></div>

                        <p>International Journal of Basic and Applied Physiology (IJBAP) is a biomedical journal with international circulation. It publishes original communications of biomedical research that advances or illuminates medical science or that
                            educates the journal readers. It is issued biannually at the time of The Annual Conference of Association of Basic and Applied Physiology.</p>

                        <p>Manuscripts dealing with Physiological aspects will be considered for publication provided they contain results of original investigations. Articles need to be of general interest – e.g., they cross the boundaries of specialties
                            or are of sufficient novelty and importance that the journal’s readers, whatever their specialty, should be made aware of the findings.</p>

                        <p>Research papers reporting original research, review articles (both narrative and evidence based), research correspondence, letter to editor will be considered. View points and Perspectives are also considered. Papers of routine
                            nature which are merely records ofinteresting cases as also those dealing with modifications of routine methodology will not be encouraged.</p>

                        <p>Further, serialization of articles by the same author(s) into various parts (1, 2, 3, etc.) is strongly discouraged. In such cases the authors are advised to submit independent papers with self-sufficient titles and text.</p>

                        <p>The IJBAP strongly discourages duplication/reduplication of data already published in other journals (even when certain cosmetic changes/additions are made). If and when duplication is detected after publishing in IJBAP, the journal
                            will be forced to ‘retract’ such articles. Articles based on work carried out in private nursing homes and other non-recognized hospitals/research institutes will be discouraged.</p>

                        <p>All papers submitted to IJBAP are subject to double blind peer review process. All accepted papers will be suitablyedited before publication.</p>

                        <p>The journal welcomes: Editorials, Original researches/studies, Review articles, Short communications, Letters to the editor, Interesting Articles, Current topics and News.</p>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>

                        <h4>Authorship Requires</h4>
                        <p>(The New ICMJE Recommendations :<a class="tipped" href="http://www.icmje.org/new_recommendations.html" target="_blank" data-title="New Recommendations" data-tipper-options='{"direction":"top"}'><b>http://www.icmje.org/new_recommendations.html</b></a>                            </p>
                        <div class="spacer-15"></div>

                        <p>(1) Substantial contributions to: the conception or design of the work; or the acquisition, analysis, or interpretation of data for the work.</p>
                        <p>(2) Drafting the work or revising it critically for important intellectual content</p>
                        <p>(3) Final approval of the version to be published</p>
                        <p>(4) Agreement to be accountable for all aspects of the work in ensuring that questions related to the accuracy or integrity of any part of the work are appropriately investigated and resolved.</p>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>
                        <h4>Submission of Manuscript</h4>
                        <div class="spacer-15"></div>
                        <p>Paper must be submitted online as per template given in our website<a class="tipped" href="www.ijbap.weebly.com" target="_blank" data-title="www.ijbap.com" data-tipper-options='{"direction":"top"}'><b>www.ijbap.com</b></a> Communications
                            intended for publication must be sent to editor on <a class="tipped" href="mailto:ijbap@yahoo.com" target="_blank" data-title="ijbap@yahoo.com" data-tipper-options='{"direction":"top"}'><b>ijbap@yahoo.com</b></a>. All manuscripts
                            are reviewed by an editor and members of the Editorial Board or qualified outside reviewers. Decisions will be made as rapidly as possible, and the journal strives to return reviewers’ comments to authors within 8 weeks.</p>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>
                        <h4>Preparation of the Manuscript</h4>
                        <div class="spacer-15"></div>
                        <p>Manuscript(for Research Paper) must be written in clear and concise English. Either British or American spelling is acceptable. It must be sent in Microsoft Word format (.doc or .docx).</p>

                        <div class="spacer-15"></div>

                        <p><b>General format of the research papers should be as follows in given template</b></p>
                        <div class="spacer-15"></div>

                        <p><b>1. Title page</b></p>
                        <p><b>2. Structured Abstract and key words</b></p>
                        <p><b>3. Introduction</b></p>
                        <p><b>4. Materials and methods</b></p>
                        <p><b>5. Results</b></p>
                        <p><b>6. Discussion</b></p>
                        <p><b>7. Conclusion</b></p>
                        <p><b>8. Acknowledgement (if any)</b></p>
                        <p><b>9. References</b></p>

                        <div class="spacer-15"></div>

                        <p><b>In general, the length of full research papers should not exceed six printed pages of the journal (each printed page will approximately contain 800 words).</b></p>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>

                        <p><b></b>The title page should include: The title of the article, which should be concise and informative. The title should normally contain no more than 150 characters (including spaces). Include the species, tissue, organ or system
                            if this is important in the context of the findings. Avoid specialist abbreviations if possible. Name of all the authors (with one forename of each author in full) followed by their affiliations: department, institution, city,
                            pincode and country. If more than one department or institution is involved, authors name should be linked to appropriate institutions/departments by inserting consecutive numbers in superscript after relevant names to which
                            the work should be attributed. Name, mailing address, fax, phone number and e-mail ID of corresponding author. Running title containing not more than 40 characters.</p>

                        <p><b></b>The second page should carry an abstract of not more than 200 words. For full research paper, the abstract should be structured into four components such as Background & objectives, Methods, Results, and Interpretation &
                            conclusion. Background and objectives should clearly but briefly mention why the present study has been taken up and also state the primary objective of the study. Methods should include basic procedures (study subjects or
                            experimental animals, observational and analytic methods) and results should contain main findings of the study (give specific data and their statistical significance, if possible). Interpretation & conclusion should clearly
                            define the outcome of the study and briefly mention the implication of the study. Use only approved abbreviations.</p>

                        <p><b></b>Below the abstract, provide key words (minimum three but not more than six) in alphabetical orders separated by coma.</p>

                        <p><b></b>Authors should avoid abbreviations unless they are easily understood and help in reading the paper. Abbreviations well known in the field do not need to be defined. Please refer to our list of well-known abbreviations and
                            symbols. All other abbreviations should be defined at their first mention and also in an alphabetical list immediately after the abstract, in the following format:</p>

                        <p><b></b>ACE2, angiotensin-converting enzyme 2; AMG, α-methyl-glucopyranoside; Ang II, angiotensin II; Ang IV, angiotensin IV; Ang (1-7), angiotensin-(1-7); AT1, angiotensin type 1 receptor; eNOS, endothelial nitric oxide synthase;
                            GAPDH, glyceraldehyde-3-phosphate dehydrogenase; L-NMMA, NG-monomethyl-L-arginine; NF-κB, nuclear factor-κB; NOS, nitric oxide synthase; PI3K, phosphatidylinositol 3-kinase; RAS, renin–angiotensin system; ROS, reactive oxygen
                            species; SGLT1, sodium-dependent glucose transporter 1; TNF-α, tumour necrosis factor-α. </p>

                        <p><b>Introduction</b>Clearly state the purpose of the study. Briefly summarize the rationale of the study and clearly indicate the lacunae or deficiencies in previous studies for which present study as been taken up. Give only pertinent
                            references. Do not review the subject extensively.</p>

                        <p><b>Materials and Methods</b>Clearly state the department or laboratories where the work was carried out. Mention whether approvals of institutional review board and/or institutional animal ethical committee were obtained prior
                            to commencement of the study. Describe your selection of the observational or experimental subjects (patients or experimental animals, including controls) clearly. Identify the methods, apparatus (manufacturer's name and address
                            in parenthesis) and procedures in sufficient detail to allow other workers to repeat the experiments. If the procedures involve an assay, include data on its accuracy, e. g. specificity, sensitivity and recovery. Give reference
                            to established methods including statistical methods. Provide references and brief descriptions for methods that have been published but are not well known. Describe new or substantially modified methods. Give reasons for using
                            them and evaluate their limitations.</p>

                        <p>When reporting experiments on human subjects, include whether the procedures followed were in accordance with the ethical standards of the committee on human experimentation of the institution in which the experiments were done
                            or in accordance with the Helsinki Declaration of 1975. While reporting experiments on animals, indicate whether the Animal Ethics Committee or the National Research Council's guidance for the care and use of laboratory animals
                            was followed. Identify precisely all drugs and chemicals used, including generic name(s), dosage(s) and route(s) of administration and mention in parenthesis manufacturer’s name and location. Do not use patient's name, initials
                            or hospital numbers. Details of statistical methods adopted for analysis of data should be described. Levels of significance should be clearly highlighted. </p>

                        <p><b>Results</b> Include number of observation and the statistical significance of the findings appropriately. Detailed statistical analyses, mathematical derivations, and the like may sometimes be suitably presented in the form
                            of one or more appendices. Present your results in logical sequence in the text, tables and illustrations. Do not repeat in the text all the data already given in tables, illustrations or both. Emphasize and summarize only
                            important observations. Each table should be typed continuously with the text. Legends for illustrations/figures should be typed continuously with the text.</p>

                        <p><b>Discussion</b> Emphasize the new and important aspects of the study and conclusions derived from them. Do not repeat in details data given in the results section. Include in the discussion the implications of the findings and
                            their limitations and relate the observations to other relevant studies. Link the conclusion with the goals of the study but avoid unqualified statements and conclusions not completely supported by your data. Avoid claiming
                            priority and alluding to work that has not been completed. Discussion should be relevant and an unnecessary lengthy presentation should be avoided.</p>

                        <p><b>Conclusion</b> A very brief summary note of the work with a concluding remark should be given. This should include the novelty and implication of the work and its contribution to the upliftment of the present scientific knowledge
                            in general. </p>

                        <p><b>References</b> Number the references consecutively in the order in which they are first mentioned in the text. Identify references in text and legends by Arabic numerals (in parentheses). Only appropriate references should be
                            cited. Generally, for a full research paper, the number of references should not exceed 40. Use the Vancouver style of referencing, as the example given below which is based on the formats used in the U.S. National Library
                            of Medicine ‘Index Medicus’. The titles of journals should be abbreviated according to the style used in Index Medicus. Any manuscript not following Vancouver system will immediately be sent back to author for revision.</p>

                        <p>Authors can get a comprehensive explanation of the system with practical examples in the following link: http://www.lib.monash.edu.au/tutorials/citing/ vancouver.html. Authors must also note that IJBAP follows following variation
                            in Vancouver style:</p>

                        <p>1. Superscripts must be used rather than brackets.</p>
                        <p>2. Numbers should be inserted to the left of colons and semi-colons and Full stops are placed after the reference number.</p>
                        <p>3. It is important that the punctuation and form is consistently applied to the whole document. </p>


                        <p>The references must be verified by the author(s) against the original documents. Examples of correct forms of reference are given below:</p>

                        <p><b>Journals</b> For standard journal article, list all authors when six or less; when seven or more, list only first three and add et al. For examples:</p>

                        <ul>
                            <li>1. Singh K. and Sood S. Effect of treatment on myocardial performance index in chronic severe anemia. Ind J Physiol Pharmacol 2007; 51: 91-95.</li>
                            <li>2. Rajan S, Balakumar S and Thirunalasundari T. Antibacterial activity of Mangifera Indica seed kernel on enteropathogenic E. coli. NJIRM 2006; 26: 25-28.</li>
                            <li>3. Pugia MJ, Sammer R, Corey P, Lott JA, Anderson L, Gleason S, et al. The uristatin dipstick is useful in distinguishing upper respiratory from urinary tract infections. Clin Chim Acta 2004; 341: 73-81.</li>
                        </ul>

                        <p><b>Books & Monographs</b></p>

                        <ul>
                            <li>1. Burrow GN. The Thyroid: nodules and neoplasia. In: Fleig P, Baxter JD, Broadus AE, Frohman LA, editors. Endocrinology and metabolism. 2nd ed. New York: McGraw-Hill. 1989: 473-507.</li>
                            <li>2. Pal GK and Pal Pravati. Brainstem auditory evoked potential. In: Text book of practical physiology. 2nd ed. Chennai: Orient Longman. 2005: 305-309.</li>
                        </ul>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>

                        <p><b>Tables:</b> Use of tables must be restricted to a minimum. Generally, each article should not have more than four tables. Do not submit tables as photographs. Number tables consecutively and provides a brief title for each.
                            Tables should be used sparingly. They should be referred to in the text by arabic numerals, e.g. Table 3. Each table should have its own self-explanatory title. The same information should not be presented in both tabular and
                            graphical forms. Tables will be processed as text and therefore should NOT be submitted as figures. </p>

                        <div class="spacer-15"></div>

                        <p><b>Illustrations:</b> Illustrative materials should be used with economy. Each copy of the manuscripts should be accompanied by one set of figures (if any). Figures should be professionally drawn and photographed; freehand or typewritten
                            lettering are unacceptable. Instead of original drawings, roentgenograms, and other materials, send at least one set of sharp, usually 3 inches or 6 inches by width and maximum length of 9 inches inclusive of legends. Letters,
                            numbers and symbols should be closer and even throughout, and of sufficient size for publications and should appear in legends for illustrations and not on the illustrations themselves. Each figure should have a label properly
                            If photographs of persons are used, either the subjects must not be identifiable or their picture must be accompanied by a written permission to use the photograph. Figures are to be also send as soft copies, they should be
                            submitted as separate JPEG, TIFF or PDF files. The scanning resolution should be 300 dpi. </p>

                        <div class="spacer-15"></div>

                        <p><b>Legends for illustrations:</b> Type legends for illustrations with arabic numerals corresponding to the illustrations. When symbols, arrow, numbers, or letters are used to identify parts of illustrations, identify and explain
                            each one clearly in the legend. </p>

                        <div class="spacer-15"></div>

                        <p><b>Units of Measurement:</b> Measurement of length, height, weight and volume should be reported in metric units (meter, kilogram, liter) or their decimal multiples or fractions. Temperatures should be given in degree Celsius.
                            Blood pressures should be given in mm of Hg. Other measurements should be reported in the units in which they were made. The following abbreviations can also be used: Unit: U; kilogram: kg; milligram: mg; picogram: pg; hour(s):
                            hr; millimole: mmol; micromole: mmol; nanomole: nmol; picomole: pmol; centimeter: cm; millimeter: mm; liter: L; milliliter: ml; micro liter: ml; microgram: mg, millimeter of mercury: mm/Hg; milliequivalent: meq, curi: ci; angstrom:
                            A; calorie: cal; temperature in degree centigrade or Fahrenheit: °C/°F; intraperitoneal, intramuscular, intravenous and subcutaneous routes of administrations as ip, im, iv and sc respectively.</p>

                        <div class="spacer-15"></div>

                        <p><b>For statistical terms:</b> Correlation coefficient: r; degrees of freedom: df; not significant: NS; sample number: n; probability (levels of significance): p; standard deviation: SD; standard error of the mean: SEM; Student’s
                            ‘t’test: t test; Variance ratio: F; Analysis of variance: ANOVA.</p>

                        <div class="spacer-15"></div>

                        <p><b>Drug names:</b> Generic drug names should be used.</p>

                        <div class="spacer-15"></div>

                        <p><b>Contractions:</b> Author should not write in contraction. For example, can’t, don’t, &, and so on. Only proper use of language serves the purpose of effective communication.</p>

                        <div class="spacer-15"></div>

                        <p><b>Acknowledgements:</b> Acknowledgements section should state person(s) / firms to whom the author has to acknowledge. For more details, please refer to Declaration of authorship responsibility, financial disclosure, competing
                            interest, copyright transfer, and acknowledgement form available along with this guideline.</p>

                        <div class="spacer-15"></div>

                        <p class="lead">Article should be submitted along with scan copy of dully filled copy right form signed by all authors and permission letter in case of diagram or photograph reproduice from other source. Prescribe copy write and authorship form
                            are available on our website. Approval letter from institutional review board and evidence of registration in CTRI should also be submitted.(<a href="http://ctri.nic.in/Clinicaltrials/login.php">http://ctri.nic.in/Clinicaltrials/login.php</a>)
                            </p>

                        <div class="spacer-15"></div>

                        <p><b>Confidentiality:</b> The policy of IJBAP is that all submissions are treated as confidential.</p>

                        <div class="spacer-15"></div>

                        <p><b>Writing style:</b> In order to make the journal uniform & standard we request that all matters submitted for publication should follow “Uniform requirements for manuscripts submitted to biomedical journals” as published by International
                            Committee of Medical Journal Editors (ICMJE) www.icmje.org </p>

                        <div class="spacer-15"></div>

                        <p><b>Title:</b> 14 point calibri and line spacing of 1</p>
                        <p><b>Name of Author:</b> 11 point calibri and line spacing of 1</p>
                        <p><b>Affiliation of Author:</b> 9 point calibri and line spacing of 1</p>
                        <p><b>Abstract:</b> 11 point calibri and line spacing of 1</p>
                        <p><b>Body text:</b> 11 point calibri and line spacing of 1.</p>
                        <p>Heading in Body Text (i.e. Introduction), Table and Figure Heading should be Bold.</p>
                        <p>The formatting of text and headings will be removed during the production process and replaced with The Journal styles.</p>

                        <div class="spacer-15"></div>

                        <p><b>Acceptance:</b> To be acceptable, papers must be technically sound, clearly written, and provide significant new data or a new physiological insight based at least in part on new data. The paper can be accepted as submitted,
                            or with only minor corrections or suggested improvements. This may be the case if a manuscript which is resubmitted as a new paper following major revision.</p>

                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>
                        <p class="lead"><b>Grounds for Rejection</b></p>

                        <ul>
                            <li>The work is unethical or does not have proper ethical permission</li>
                            <li>The work described is not original. This includes work that is basically an expansion of the authors' results published elsewhere </li>
                            <li>The work is unsound, there are flaws in the design of experiments or in the analysis of data</li>
                            <li>The conclusions are inadequately supported by the results</li>
                            <li>There is evidence in the literature that invalidates key elements of the work</li>
                            <li>The paper does not significantly further physiological understanding</li>
                            <li>The observations are too specialised and of little interest to physiologists</li>
                            <li>The phenomena described are of no clear physiological relevance</li>
                            <li>The paper is badly arranged or poorly written.</li>
                            <li>The data cannot be assessed because the figures are so badly presented</li>
                            <li>Purely theoretical papers, unless based on experimentally derived data and the hypothesis advanced is directly amenable to experimental testing </li>
                            <li>Papers on functions of invertebrates, or lower vertebrates, that have no counterparts among higher vertebrates and are unlikely to elucidate functions of the latter </li>
                        </ul>
                        <div class="spacer-15"></div>
                        <div class="spacer-15"></div>

                        <p class="lead"><b>Grounds for Rejection</b></p>
